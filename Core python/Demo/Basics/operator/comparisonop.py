# Compariosn operator is also called relational operator
x = 10
y = 20

#1.  ==
#Exact equals to
print (x == y)

#2. !=
# not equals to
print(x != y)

#3. >
#greater than
print(x>y)

#4. < 
# less than
print(x<y)

#5. >=
# greater than equals to
print(x >= y)

#6. <=
# Less than equals to
print(x <= y)



